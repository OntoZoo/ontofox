<h3 class="head3_darkred">News</h3>
<br/>
<ul>
  <li><strong>Dec 20, 2016:</strong> OntoFox is renamed to Ontofox for consistency with other Ontoanimal tools.</li>
  <li><strong>Dec 10, 2011:</strong> All the OBO ontologies have changed the term URI format from http://purl.org/obo/owl/ontology#ontology_nnnnnnn to http://purl.obolibrary.org/obo/ontology_nnnnnnn. Please make sure your input files are updated. </li>
  <li><strong>Dec 1, 2011:</strong> Ontofox now use merged version of the ontologies. </li>
  <li><strong>Dec 1, 2011:</strong> <a href="http://ontodog.hegroup.org">Ontodog(previously Ontofox-View, Ontofoxo, OntoMod)</a> is online.</li>
  <li><strong>July 15, 2011:</strong> Oliver and Allen generated a new tutorial page for <a href="tutorial/import_Ontofox_output_file.php">how to import Ontofox output owl file into a target ontology</a>. </li>
  <li><strong>July 13, 2011:</strong> Oliver  changed the name &quot;Ontofoxo&quot; to Ontofox-View, representing: Ontofox-based Community View Generator. </li>
  <li><strong>July 7, 2011:</strong> Oliver changed the name  <a href="http://ontodog.hegroup.org">OntoMod</a> to OntofoxO, representing Ontofox-based Ontology &quot;Orthorization&quot;. Oliver coined and described the name &quot;orthorization&quot; to differ it from ontology &quot;modularization&quot;.</li>
  <li><strong>June 23, 2011:</strong> The Ontofox-based ontology modularization tool was renamed <a href="http://ontodog.hegroup.org">OntoMod</a>.</li>
  <li><strong>June 22, 2011</strong>: An Ontofox-based modularization tool  was  online, developed by Allen and Oliver, based on Jie Zheng and Chris Stoeckert's <a href="http://www.mged.org/">FGED</a> community view use case study. </li>
  <li><strong>June 9, 2011</strong>: Inclusion of owl:equivalentClass is now a separated option.</li>
  <li> <strong>Apr 5, 2011</strong>: Updated version  of Ontofox (1.1) can be downloaded from <a href="http://ontofox.googlecode.com/files/ontofox1.1.zip">Ontofox Google code website</a>.</li>
  <li> <strong>June, 2010</strong>: Ontofox paper published. Xiang Z, Courtot M, Brinkman RR, Ruttenberg A, <strong>He Y</strong>. <a href="http://www.biomedcentral.com/1756-0500/3/175">Ontofox: web-based support for ontology reuse</a>. <em>BMC Research Notes</em>. 2010, <strong>3:</strong>175. PMID: <a href="http://www.ncbi.nlm.nih.gov/pubmed/20569493">20569493</a>. PMCID: <a href="http://www.ncbi.nlm.nih.gov/pmc/articles/PMC2911465/?tool=pubmed">PMC2911465</a>. The <a href="http://www.biomedcentral.com/content/supplementary/1756-0500-3-175-s3.zip">source code of Ontofox version 1.0</a> and be downloaded from BMC.</li>
  <li><strong>July, 2009</strong>: Ontofox server is online.</li>
  </ul>
